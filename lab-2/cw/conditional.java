public class conditional {
    public static void main(String[] args) {
     
        int a = 120, b = 60;
        int largest;
        
        System.out.println();
        System.out.println("a = " + a + " b = " + b);
        System.out.println();
        System.out.print("Largest num using conditional operator is: ");
        largest = (a > b) ? a : b;
        System.out.println(largest);

    }
}
